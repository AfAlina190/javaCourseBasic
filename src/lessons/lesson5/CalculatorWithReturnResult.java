package lessons.lesson5;

public class CalculatorWithReturnResult {
    public int sum(int k, int l ){
        int result = k + l;
        return result;
    }
    public int subtract(int k, int l ){
        int result = k - l;
        return result;
    }
    public int multiply(int k, int l ){
        int result = k * l;
        return result;
    }
    public int divide(int k, int l ){
        int result = k / l;
        return result;
    }
}


